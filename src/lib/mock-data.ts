export const mockTimeSeries: TimeSeriesPoint[] = Array.from(
  { length: 30 },
  (_, i) => {
    const dayOffset = 29 - i;
    const day = new Date(Date.now() - dayOffset * MS_DAY);
    const iso = day.toISOString().slice(0, 10);
    const wave = Math.sin((i / 30) * Math.PI * 2) * 0.35;
    const orders = Math.max(
      12,
      Math.round(26 + wave * 14 + (i % 4) * 3 + (dayOffset % 7)),
    );
    const avgOrderValue = 68 + (i % 9) * 7 + Math.round(wave * 20);
    const revenue = Math.round(orders * avgOrderValue);
    const tickets = Math.max(
      2,
      Math.round(6 + Math.cos((i / 9) * Math.PI) * 4 + (i % 3)),
    );
    return { date: iso, orders, revenue, tickets };
  },
);

