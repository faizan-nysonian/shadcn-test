const MS_HOUR = 60 * 60 * 1000;
const MS_DAY = 24 * MS_HOUR;

function daysAgo(days: number, hourOfDay = 14): Date {
  const d = new Date(Date.now() - days * MS_DAY);
  d.setHours(hourOfDay, Math.floor((days * 17) % 60), 0, 0);
  return d;
}

function hoursAgo(hours: number): Date {
  return new Date(Date.now() - hours * MS_HOUR);
}

